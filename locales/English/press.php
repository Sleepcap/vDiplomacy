<p class="intro">
The game of Diplomacy offers much in the way of variants. While some of these are more simple map variants or more complex rule variations,
there exist several different communication systems available which are outlined below:
</p>

<div class="hr" ></div>

<a name="fullPress"></a>
<p class="intro">
<h3 style="margin-left:0px;">Full Press:</h3>
Full Press is the standard type of press. In Full Press games, all players can send each other private messages.
In this game mode, you can never be sure what the other players are saying to each other and what kind of secret plans
are being made. Full Press is the pinnacle of plotting and intrigue.
Try to work out your plans with a select number of other players and try to not be surprised by unexpected alliances
that might be forming against you.</p>

<div class="hr" ></div>

<a name="publicPress"></a>
<p class="intro">
<h3 style="margin-left:0px;">Public Press:</h3>
No private messages can be sent in this game mode. The only type of messages that are possible are so-called global messages,
which are messages that every player can see. No secret plans can be made, as every player can read everything that all the
others are saying. Public Press games will involve large discussions that every player can take part in. 
Try to sway public opinion in your favor and persuade other players to work with you without antagonizing too many others.</p>

<div class="hr" ></div>

<a name="gunboat"></a>
<p class="intro">
<h3 style="margin-left:0px;">Gunboat:</h3>
Gunboat is the game mode in which no messages are allowed at all.
This does not mean that it is impossible to form alliances, though; it just means that alliances have to be formed without the use of words.
Players can signal that they have friendly intentions towards someone by simply not attacking or even by trying to support units of other
players. Try to pay close attention to the map and the orders to see what the other players are up to and try to unravel their intentions.
At the same time, keep in mind what other players will think of you when they observe your moves. Note that failed support orders 
are only visible on the large map, so you may want to look at that.</p>

<div class="hr" ></div>

<a name="rulebook"></a>
<p class="intro">
<h3 style="margin-left:0px;">Rulebook Press:</h3>
Rulebook Press is Full Press with a twist: You can send private messages to all players, except during retreat and build phases,
where no messaging is possible! According to the rulebook of the board game of Diplomacy, there are no negotiation phases during
builds and retreats, either; hence why this game mode is called Rulebook Press. 
</p>
